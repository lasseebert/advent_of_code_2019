defmodule Advent.Day20Test do
  use Advent.Test.Case

  alias Advent.Day20

  describe "part 1" do
    test "example 1" do
      input = """
               A           
               A           
        #######.#########  
        #######.........#  
        #######.#######.#  
        #######.#######.#  
        #######.#######.#  
        #####  B    ###.#  
      BC...##  C    ###.#  
        ##.##       ###.#  
        ##...DE  F  ###.#  
        #####    G  ###.#  
        #########.#####.#  
      DE..#######...###.#  
        #.#########.###.#  
      FG..#########.....#  
        ###########.#####  
                   Z       
                   Z       
      """

      assert Day20.shortest_path(input) == 23
    end

    test "example 2" do
      input = """
                         A               
