        ###.###########.###.#####.#.#.#  
        #.....#        A   C    #.#.#.#  
        #######        S   P    #####.#  
        #.#...#                 #......VT
        #.#.#.#                 #.#####  
        #...#.#               YN....#.#  
        #.###.#                 #####.#  
      DI....#.#                 #.....#  
        #####.#                 #.###.#  
      ZZ......#               QG....#..AS
        ###.###                 #######  
      JO..#.#.#                 #.....#  
        #.#.#.#                 ###.#.#  
        #...#..DI             BU....#..LF
        #####.#                 #.#####  
      YN......#               VT..#....QG
        #.###.#                 #.###.#  
